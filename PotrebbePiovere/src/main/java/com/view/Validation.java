package com.view;

public class Validation {
	
	int idInput; 
	int idInputModal;
	String nameInputModal;
	String idCheckbox = "";
	String nameCheckbox = "";
	String lang;
	String units;	
	
	
	public void setIdInput(int idInput) {
		this.idInput = idInput;
	}
	public void setIdInputModal(int idInputModal) {
		this.idInputModal = idInputModal;
	}
	public void setNameInputModal(String nameInputModal) {
		this.nameInputModal = nameInputModal;
	}
	public void setIdCheckbox(String idCheckbox) {
		this.idCheckbox = idCheckbox;
	}
	public void setNameCheckbox(String nameCheckbox) {
		this.nameCheckbox = nameCheckbox;
	}
	public void setLang(String lang) {
		this.lang = lang;
	}
	public void setUnits(String units) {
		this.units = units;
	}
	
	
	public int getIdInput() {
		return idInput;
	}
	public int getIdInputModal() {
		return idInputModal;
	}
	public String getNameInputModal() {
		return nameInputModal;
	}
	public String getIdCheckbox() {
		return idCheckbox;
	}
	public String getNameCheckbox() {
		return nameCheckbox;
	}
	public String getLang() {
		return lang;
	}
	public String getUnits() {
		return units;
	}

	
	

}